'''Se necesita obtener el promedio simple de un estudiante a partir de sus tres notas parciales'''

nota1 = int(input('Ingresa la primera nota del estudiante: '))
nota2 = int(input('Ingresa la segunda nota del estudiante: '))
nota3 = int(input('Ingresa la tercera nota del estudiante: '))
print("El promedio de las notas del estudiante es de ", ((nota1+nota2+nota3)/3), " puntos.")
